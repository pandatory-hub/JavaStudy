public class Q6_1_1 {
  public static void result(int a, int b) {
    System.out.println("a+b=" + (a + b));
    System.out.println("a-b=" + (a - b));
    System.out.println("a*b=" + (a * b));
    System.out.println("a/b=" + (a / b));
    System.out.println("a%b=" + (a % b));
  }

  public static void main(String[] args) {
    result(10, 8);
  }
}
